package enums;

/**
 * Enum representing different user role.
 * Each constant represents a user role, namely Staff, Student and Camp Committee.
 */
public enum UserRole {
	
	/**
     * Denotes that user is a student in NTU.
     */
	STUDENT,
	
	/**
     * Denotes that user is a student and a camp committee member of a camp in NTU.
     */
	COMMITTEE,
	
	/**
     * Denotes that user is a staff in NTU.
     */
	STAFF}
